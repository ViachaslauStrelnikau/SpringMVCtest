package model;

public class Invoice {
    private String invoiceInvoice;
    private int invoiceT_weight;
    private int invoiceN_weight;
    private int invoiceN_pack;

    public String getInvoiceInvoice() {
        return invoiceInvoice;
    }

    public int getInvoiceT_weight() {
        return invoiceT_weight;
    }

    public int getInvoiceN_weight() {
        return invoiceN_weight;
    }

    public int getInvoiceN_pack() {
        return invoiceN_pack;
    }

    public void setInvoiceInvoice(String invoiceInvoice) {
        this.invoiceInvoice = invoiceInvoice;
    }

    public void setInvoiceT_weight(int invoiceT_weight) {
        this.invoiceT_weight = invoiceT_weight;
    }

    public void setInvoiceN_weight(int invoiceN_weight) {
        this.invoiceN_weight = invoiceN_weight;
    }

    public void setInvoiceN_pack(int invoiceN_pack) {
        this.invoiceN_pack = invoiceN_pack;
    }

}
